weather = 'sunny'


case weather
when 'raining' 
	puts "Wear rubber boots!"
when 'sunny' 
	puts "Wear a nice sun dress!"
when 'cloudy' 
	puts "Best grab a rain jacket to be on the safe side!"
when 'snowing' 
	puts "Best to stay inside :)"
else 
	puts "Who know's? Best to be prepared for ANYTHING!"
end
